CREATE TABLE Resume
  (
      ID int,
      NAME2 varchar(100) NOT NULL,
      SURNAME varchar(100) NOT NULL,
      PATRONYMIC varchar(100) NOT NULL,
      SEX varchar(50) NOT NULL,
      Date_of_Birth date,
      GITHUB_CONTACTS varchar(100),
      EMAIL varchar(100),
      TELEPHONE varchar(100),
      SKYPE varchar(100),
      LINKEDIN varchar(100),
      GITHUB_TECHNOLOGY varchar(100),
      SPRING_BOOT varchar(100),
      HTML varchar(100),
      JAVA_EE varchar(100),
      JAVA_CORE varchar(100),
      MAVEN varchar(100),
      REST varchar(100),
      SPRING varchar(100),
      PRIMARY KEY (ID)
  );
